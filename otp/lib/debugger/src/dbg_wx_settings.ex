  def load(win, pos, sFile) do
    str = ~c"Load Settings Dialog"
    open_win(win, pos, sFile, str, 1)
  end

  def save(win, pos, sFile) do
    str = ~c"Save Settings Dialog"
    open_win(win, pos, sFile, str, 2)
  end

  defp open_win(win, pos, sFile, str, what) do
    {sDir, sFileName} =
      cond do
        sFile == :default ->
          {default_settings_dir(win), ~c"NoName.state"}

        true ->
          {:filename.dirname(sFile), :filename.basename(sFile)}
      end

    fD =
      :wxFileDialog.new(
        win,
        [
          {:message, str},
          {:pos, pos},
          {:defaultDir, sDir},
          {:defaultFile, sFileName},
          {:wildCard, ~c"*.state"},
          {:style, what}
        ]
      )

    case :wxFileDialog.showModal(fD) do
      5100 ->
        newFile = :wxFileDialog.getPath(fD)
        {:ok, newFile}

      _ ->
        :wxFileDialog.destroy(fD)
        :cancel
    end
  end

  defp default_settings_dir(win) do
    {:ok, [[home]]} = :init.get_argument(:home)
    defDir = :filename.join([home, ~c".erlang_tools", ~c"debugger"])

    case :filelib.is_dir(defDir) do
      true ->
        defDir

      false ->
        xDGDir =
          :filename.join(
            :filename.basedir(
              :user_config,
              ~c"erlang"
            ),
            ~c"debugger"
          )

        case :filelib.is_dir(xDGDir) do
          true ->
            xDGDir

          false ->
            {:ok, cWD} = :file.get_cwd()

            msg = [
              ~c"Default directory ",
              xDGDir,
              ~c" does not exist. ",
              ~c"Click OK to create it or ",
              ~c"Cancel to use other directory."
            ]

            case :dbg_wx_win.confirm(win, msg) do
              :ok ->
                toolsDir = :filename.dirname(xDGDir)

                case :filelib.is_dir(toolsDir) do
                  true ->
                    case :file.make_dir(xDGDir) do
                      :ok ->
                        xDGDir

                      _Error ->
                        cWD
                    end

                  false ->
                    case :file.make_dir(toolsDir) do
                      :ok ->
                        case :file.make_dir(xDGDir) do
                          :ok ->
                            xDGDir

                          _Error ->
                            cWD
                        end

                      _Error ->
                        cWD
                    end
                end

              :cancel ->
                cWD
            end
        end
    end
  end
